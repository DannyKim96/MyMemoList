package com.example.mymemolist;

public class Memo {
    private int memoID;
    private String memoData;

    public Memo(){

    }

    public int getMemoID() {
        return memoID;
    }

    public void setMemoID(int memoID) {
        this.memoID = memoID;
    }

    public String getMemoData() {
        return memoData;
    }

    public void setMemoData(String memoData) {
        this.memoData = memoData;
    }
}
